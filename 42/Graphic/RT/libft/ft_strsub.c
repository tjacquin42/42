#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char *str;

	if (!s)
		return (NULL);
	str = ft_memalloc(len + 1);
	if (str != NULL)
		ft_memcpy(str, &s[start], len);
	return (str);
}
